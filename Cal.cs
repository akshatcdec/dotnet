namespace StaticDemo
{
    public static class Cal
    {
       public static int Add(int x,int y)
        {
            return x + y;
        }
        public static int Div(int x, int y)
        {
            return x / y;
        }
        public static int Multi(int x, int y)
        {
            return x * y;
        }
        public static int Sub(int x, int y)
        {
            return x - y;
        }
    }
}
